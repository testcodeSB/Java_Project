package project3;

public class StringMethods {

	public static void main(String[] args) {
		String a="I am a good boy";
		System.out.println(a.length());
		System.out.println(a.toUpperCase());
		System.out.println(a.toLowerCase());
		System.out.println(a.startsWith("I"));
		System.out.println(a.endsWith("boy"));
		System.out.println(a.contains("go"));
		System.out.println(a.concat(" in my home"));
		System.out.println(a.charAt(14));
		System.out.println(a.substring(10));
		System.out.println(a.substring(7,14));
		
		
		
		
		
		

	}

}
